class GameContractHelper {
  constructor(provider) {
    this.provider = provider;
    this.contracts = {};
  }

  async init(addresses) {
    try {
      // Initialize contracts with ABIs
      this.contracts.gameToken = new ethers.Contract(
        addresses.gameToken,
        GameTokenABI,
        this.provider
      );

      this.contracts.rewardPool = new ethers.Contract(
        addresses.rewardPool,
        RewardPoolABI,
        this.provider
      );

      this.contracts.nft = new ethers.Contract(
        addresses.nft,
        SpacePuzzleNFTABI,
        this.provider
      );

      // Get token decimals
      this.decimals = await this.contracts.gameToken.decimals();

      return true;
    } catch (error) {
      console.error("Error initializing contracts:", error);
      throw error;
    }
  }

  // Helper methods for amount formatting
  parseAmount(amount) {
    return ethers.utils.parseUnits(amount, this.decimals);
  }

  formatAmount(amount) {
    return ethers.utils.formatUnits(amount, this.decimals);
  }

  // Contract interaction methods
  async tokenizeKeys(amount, signer) {
    const contract = this.contracts.gameToken.connect(signer);
    return await contract.tokenizeKeys(amount);
  }

  async detokenizeKeys(amount, signer) {
    const contract = this.contracts.gameToken.connect(signer);
    return await contract.detokenizeKeys(amount);
  }

  async getKeyBalance(address) {
    return await this.contracts.gameToken.balanceOf(address);
  }

  async claimDailyReward(signer) {
    const contract = this.contracts.rewardPool.connect(signer);
    return await contract.claimDailyReward();
  }

  async canClaimReward(address) {
    return await this.contracts.rewardPool.canClaimReward(address);
  }

  async getLastClaimTime(address) {
    return await this.contracts.rewardPool.getLastClaimTime(address);
  }

  async getConsecutiveDays(address) {
    return await this.contracts.rewardPool.getConsecutiveDays(address);
  }

  async getNextRewardAmount(address) {
    return await this.contracts.rewardPool.getNextRewardAmount(address);
  }

  async isAchievementUnlocked(address, achievementId) {
    return await this.contracts.nft.isAchievementUnlocked(
      address,
      achievementId
    );
  }

  async getAchievementDetails(achievementId) {
    return await this.contracts.nft.getAchievementDetails(achievementId);
  }

  async getPlayerAchievements(address) {
    return await this.contracts.nft.getPlayerAchievements(address);
  }

  // Key Management Functions
  async hasEnoughKeys(playerAddress, amount) {
    return this.contracts.gameToken.hasEnoughKeys(playerAddress, amount);
  }

  async burnKeys(playerAddress, amount, gameSigner) {
    const tx = await this.contracts.gameToken
      .connect(gameSigner)
      .burnKeys(playerAddress, amount);
    return tx.wait();
  }

  async mintKeys(playerAddress, amount, gameSigner) {
    const tx = await this.contracts.gameToken
      .connect(gameSigner)
      .mintKeys(playerAddress, amount);
    return tx.wait();
  }

  // Achievement Functions
  async createAchievement(
    name,
    description,
    rarity,
    requiredScore,
    requiredLevel,
    adminSigner
  ) {
    const tx = await this.contracts.nft
      .connect(adminSigner)
      .createAchievement(
        name,
        description,
        rarity,
        requiredScore,
        requiredLevel
      );
    return tx.wait();
  }

  async mintAchievement(playerAddress, achievementId, adminSigner) {
    const tx = await this.contracts.nft
      .connect(adminSigner)
      .mintAchievement(playerAddress, achievementId);
    return tx.wait();
  }

  // Event Listeners
  setupEventListeners(callbacks = {}) {
    // GameToken Events
    this.contracts.gameToken.on("KeysTokenized", (player, amount) => {
      if (callbacks.onKeysTokenized) callbacks.onKeysTokenized(player, amount);
    });

    this.contracts.gameToken.on("KeysDetokenized", (player, amount) => {
      if (callbacks.onKeysDetokenized)
        callbacks.onKeysDetokenized(player, amount);
    });

    // RewardPool Events
    this.contracts.rewardPool.on(
      "RewardClaimed",
      (player, amount, consecutiveDays) => {
        if (callbacks.onRewardClaimed)
          callbacks.onRewardClaimed(player, amount, consecutiveDays);
      }
    );

    // NFT Events
    this.contracts.nft.on(
      "AchievementUnlocked",
      (player, achievementId, tokenId) => {
        if (callbacks.onAchievementUnlocked)
          callbacks.onAchievementUnlocked(player, achievementId, tokenId);
      }
    );
  }
}

// Make GameContractHelper available globally
window.GameContractHelper = GameContractHelper;
